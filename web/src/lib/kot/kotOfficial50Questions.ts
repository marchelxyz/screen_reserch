import type { KotQuestionKey } from "@/lib/kot/step1Types";

export type KotMcOption = {
  readonly id: string;
  readonly label: string;
};

export type KotOfficialSpec =
  | {
      readonly key: KotQuestionKey;
      readonly kind: "mc";
      readonly prompt: string;
      readonly options: readonly KotMcOption[];
    }
  | {
      readonly key: KotQuestionKey;
      readonly kind: "text";
      readonly prompt: string;
      readonly placeholder?: string;
    };

function mc(
  key: KotQuestionKey,
  prompt: string,
  options: readonly KotMcOption[]
): KotOfficialSpec {
  return { key, kind: "mc", prompt, options };
}

function text(
  key: KotQuestionKey,
  prompt: string,
  placeholder?: string
): KotOfficialSpec {
  return { key, kind: "text", prompt, placeholder };
}

